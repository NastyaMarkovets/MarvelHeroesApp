import UIKit

extension UIColor {
  class func customBlue() -> UIColor {
    return UIColor(red: 66.0/255.0, green: 143.0/255.0, blue: 222.0/255.0, alpha: 1.0)
  }
  
  class func customGray() -> UIColor {
    return UIColor(red: 77.0/255.0, green: 77.0/255.0, blue: 77.0/255.0, alpha: 1.0)
  }
  
  class func customGreen() -> UIColor {
    return UIColor(red: 0/255.0, green: 160.0/255.0, blue: 0/255.0, alpha: 1.0)
  }
  
}
